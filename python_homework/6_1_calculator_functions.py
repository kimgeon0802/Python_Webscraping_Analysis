'''
두 수를 받아서 사칙연산을 수행하는 함수들을 작성하는 프로그램을 만드세요.
출력결과:
10 + 5 = 15
10 - 5 = 5
10 * 5 = 50
10 / 5 = 2.0
'''

number1 = 10
number2 = 5

print(f'{number1} + {number2} = {number1+number2}')
print(f'{number1} - {number2} = {number1-number2}')
print(f'{number1} * {number2} = {number1*number2}')
print(f'{number1} / {number2} = {number1/number2}')